def build_ppt(module_id, build_func, filename):
    """Generic PPT builder: create, build, save"""
    prs = Presentation()
    prs.slide_width  = Cm(25.4)  # 16:9 widescreen
    prs.slide_height = Cm(19.05)
    build_func(prs)
    filepath = os.path.join(OUTPUT_DIR, filename)
    prs.save(filepath)
    print(f'  ✅ {module_id}: {filename} ({len(prs.slides)}页) → {filepath}')
    return filepath


