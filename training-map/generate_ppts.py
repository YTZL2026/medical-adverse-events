# ======================== HELPER FUNCTIONS ========================
def add_slide_bg(prs, color=WHITE):
    """Add a blank slide with background color"""
    slide_layout = prs.slide_layouts[6]  # blank
    slide = prs.slides.add_slide(slide_layout)
    bg = slide.background
    fill = bg.fill
    fill.solid()
    fill.fore_color.rgb = color
    return slide

def add_textbox(slide, left, top, width, height, text, font_size=18,
                bold=False, color=BLACK, alignment=PP_ALIGN.LEFT, font_name=FONT_BODY):
    """Add a text box with single text"""
    txBox = slide.shapes.add_textbox(Cm(left), Cm(top), Cm(width), Cm(height))
    tf = txBox.text_frame
    tf.word_wrap = True
    p = tf.paragraphs[0]
    p.text = text
    p.font.size = Pt(font_size)
    p.font.bold = bold
    p.font.color.rgb = color
    p.font.name = font_name
    p.alignment = alignment
    return txBox

def add_bullet_list(slide, left, top, width, height, items, font_size=14,
                    color=BLACK, line_spacing=1.5):
    """Add a text box with bullet points"""
    txBox = slide.shapes.add_textbox(Cm(left), Cm(top), Cm(width), Cm(height))
    tf = txBox.text_frame
    tf.word_wrap = True
    for i, item in enumerate(items):
        if i == 0:
            p = tf.paragraphs[0]
        else:
            p = tf.add_paragraph()
        p.text = item
        p.font.size = Pt(font_size)
        p.font.color.rgb = color
        p.font.name = FONT_BODY
        p.space_after = Pt(font_size * (line_spacing - 1))
        p.level = 0
    return txBox

def add_accent_bar(slide, left, top, width, height, color=BRAND_BLUE):
    """Add a colored accent bar"""
    shape = slide.shapes.add_shape(MSO_SHAPE.RECTANGLE, Cm(left), Cm(top), Cm(width), Cm(height))
    shape.fill.solid()
    shape.fill.fore_color.rgb = color
    shape.line.fill.background()
    return shape

def add_header_bar(slide, title_text, subtitle_text=None):
    """Standard header bar for content slides"""
    add_accent_bar(slide, 0, 0, 25.4, 1.8, BRAND_DARK)
    add_textbox(slide, 1.5, 0.2, 22, 1.2, title_text, font_size=24, bold=True, color=WHITE, font_name=FONT_TITLE)
    if subtitle_text:
        add_textbox(slide, 1.5, 1.2, 22, 0.6, subtitle_text, font_size=11, color=RGBColor(0xBB,0xCC,0xDD))

def add_footer(slide, page_num):
    """Standard footer"""
    add_accent_bar(slide, 0, 18.2, 25.4, 0.8, BRAND_DARK)
    add_textbox(slide, 1.5, 18.2, 10, 0.6, '辽宁中医嘉和医院 · 标准化培训', font_size=8, color=RGBColor(0x99,0xAA,0xBB))
    add_textbox(slide, 21, 18.2, 3, 0.6, str(page_num), font_size=8, color=RGBColor(0x99,0xAA,0xBB), alignment=PP_ALIGN.RIGHT)

def make_cover(prs, title, subtitle, domain_icon, date_str='2026年'):
    """Create a cover slide"""
    slide = add_slide_bg(prs, WHITE)
    add_accent_bar(slide, 0, 0, 25.4, 10, BRAND_DARK)
    add_textbox(slide, 2, 2, 5, 3, domain_icon, font_size=48, color=WHITE)
    add_textbox(slide, 2, 5, 21, 2, title, font_size=32, bold=True, color=WHITE, font_name=FONT_TITLE)
    add_textbox(slide, 2, 7.5, 21, 1.5, subtitle, font_size=14, color=RGBColor(0xCC,0xDD,0xEE))
    add_accent_bar(slide, 2, 9.5, 4, 0.15, BRAND_GOLD)
    add_textbox(slide, 2, 10.5, 10, 1, '辽宁中医嘉和医院', font_size=16, bold=True, color=WHITE)
    add_textbox(slide, 2, 11.5, 10, 1, f'标准化培训体系 · {date_str}', font_size=11, color=RGBColor(0xAA,0xBB,0xCC))
    return slide

def make_objectives_slide(prs, objectives, page_num):
    """Create a training objectives slide"""
    slide = add_slide_bg(prs, WHITE)
    add_header_bar(slide, '🎯 培训目标', '培训结束后，参训人员能够：')
    items = [f'{i+1}. {obj}' for i, obj in enumerate(objectives)]
    add_bullet_list(slide, 2, 2.5, 21, 14, items, font_size=14, color=BLACK)
    add_footer(slide, page_num)
    return slide

def make_section_title(prs, section_title, section_subtitle, page_num):
    """Create a section divider slide"""
    slide = add_slide_bg(prs, BRAND_DARK)
    add_textbox(slide, 3, 6, 20, 2, section_title, font_size=28, bold=True, color=WHITE, font_name=FONT_TITLE)
    add_accent_bar(slide, 3, 8.5, 4, 0.15, BRAND_GOLD)
    add_textbox(slide, 3, 9, 20, 1.5, section_subtitle, font_size=13, color=RGBColor(0xAA,0xBB,0xCC))
    add_footer(slide, page_num)
    return slide

def make_content_slide(prs, title, bullets, page_num, note=None):
    """Create a standard content slide with bullet points"""
    slide = add_slide_bg(prs, WHITE)
    add_header_bar(slide, title, note)
    add_bullet_list(slide, 1.5, 2.5, 22, 14, bullets, font_size=14)
    add_footer(slide, page_num)
    return slide

def make_closing_slide(prs, summary_text, page_num):
    """Create a closing/thank you slide"""
    slide = add_slide_bg(prs, BRAND_DARK)
    add_textbox(slide, 3, 5, 20, 2, '📋 培训总结', font_size=28, bold=True, color=WHITE, font_name=FONT_TITLE)
    add_accent_bar(slide, 3, 7.5, 4, 0.15, BRAND_GOLD)
    add_textbox(slide, 3, 8, 20, 3, summary_text, font_size=14, color=RGBColor(0xCC,0xDD,0xEE))
    add_textbox(slide, 3, 12, 20, 1.5, '感谢聆听 · 病历无小事，培训靠大家', font_size=18, bold=True, color=BRAND_GOLD)
    add_footer(slide, page_num)
    return slide

